package cn.ifxcode.vo;

public class BlogTopicTime{
	
	private Integer blogcount;
	private String timeclassify;
	private String timedetail;
	
	public Integer getBlogcount() {
		return blogcount;
	}
	
	public void setBlogcount(Integer blogcount) {
		this.blogcount = blogcount;
	}

	public String getTimeclassify() {
		return timeclassify;
	}

	public void setTimeclassify(String timeclassify) {
		this.timeclassify = timeclassify;
	}

	public String getTimedetail() {
		return timedetail;
	}

	public void setTimedetail(String timedetail) {
		this.timedetail = timedetail;
	}

}
